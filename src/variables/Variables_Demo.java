package variables;

public class Variables_Demo {
    public static void main(String[] args) {
        // DataType variable_name = value

        // put String value
        String name= "Java ";
        System.out.println("Name="+name);
        String book="Hound of the Baskervill";
        System.out.println("Book ="+book);
        String school= "M.s School";
        System.out.println("School="+school);

        // put integer value
        int roll_no= 564;
        System.out.println("roll no="+roll_no);
        int age = 22;
        System.out.println("Age ="+age);
        int school_code= 342;
        System.out.println("School code="+school_code);

        // put double value
        double price= 7654.45;
        System.out.println("price="+price);
        double salary= 5487.54;
        System.out.println("Salary= "+salary);
        double mobile_price= 4300;
        System.out.println("Mobile price="+mobile_price);

    }
}
