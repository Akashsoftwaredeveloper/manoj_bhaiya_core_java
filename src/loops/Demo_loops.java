package loops;

public class Demo_loops {
    public static void main(String[] args) {
          //loops increment
        for (int i=0; i<5;i++){

            System.out.println("Printing Hello......."+i);
        }
        // loops decrement
        for(int k=8; k>0; k--){
            System.out.println("Hello SpringBoot....."+k);
        }
        // loops increment
        for (int p=0; p<8;p++){
            System.out.println("Printing value of p..."+p);
        }
        //  loops decrement
        for(int q=2; q<8; q++) {

            System.out.println("Printing core java" + q);
        }
    }
}
